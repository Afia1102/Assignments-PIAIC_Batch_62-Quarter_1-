#Basic Calculator Operations in Python

input_1=int(input("Enter first number="))
input_2=int(input("Enter second number="))
operation=input("Enter the operation(+,-,*,/)")

if operation=='+':
    return input_1 + input_2
elif operation=='-':
    return input_1 - input_2
elif operation=='*':
    return input_1 - input_2
elif operation=='/':
    return input_1 - input_2
